package com.company;

public class Persona {
    public String nombre;
    public int edad;
    public int dni;
    public int telefono;
    public String domicilio;

    public Persona(String nombre, int edad, int dni, int telefono, String domicilio) {
        this.nombre = nombre;
        this.edad = edad;
        this.dni = dni;
        this.telefono = telefono;
        this.domicilio = domicilio;
    }
}
